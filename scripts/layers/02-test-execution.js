/**
 * Layer 2: Targeted Test Execution
 *
 * The key insight: don't run ALL tests — run only the tests that
 * exercise the changed code. This is fast AND high-signal.
 *
 * How it works:
 * 1. Collect all test files (hand-written + Qure-generated)
 * 2. Impact analysis: which tests reference the changed files/exports?
 * 3. Run ONLY those affected tests
 * 4. Capture: pass/fail, which assertions broke, coverage delta
 *
 * This produces FACTS: "test X failed" or "test X passed but doesn't
 * cover the changed lines" — not LLM speculation.
 */

const { execSync, spawnSync } = require("child_process");
const fs = require("fs");
const path = require("path");

// Qure detection markers
const QURE_MARKERS = [
  /generated by qure/i,
  /qure.*recording/i,
  /qure.*flow/i,
  /\.qure$/i,
  /@qure/i,
];

async function runLayer2_TestExecution(changedFiles, config) {
  const results = {
    affectedTests: [],
    executed: false,
    passed: [],
    failed: [],
    skipped: [],
    coverageDelta: null,
    qureResults: { passed: [], failed: [] },
    summary: "",
  };

  // ─── Collect all test files ───────────────────────────────────────────────────
  const allTests = collectTestFiles(config);
  console.log(`  Found ${allTests.length} total test file(s)`);

  if (allTests.length === 0) {
    results.summary = "No test files found.";
    return results;
  }

  // ─── Impact analysis: which tests are affected? ───────────────────────────────
  const affected = findAffectedTests(allTests, changedFiles);
  results.affectedTests = affected;

  if (affected.length === 0) {
    console.log("  No tests directly reference the changed files.");
    results.summary = "No tests directly reference the changed files. Coverage gap detected.";
    return results;
  }

  console.log(`  ${affected.length} test(s) affected by this change:`);
  affected.forEach((t) => {
    const source = t.isQure ? "[Qure]" : "[hand-written]";
    console.log(`    ${source} ${path.basename(t.filePath)}`);
  });

  // ─── Execute affected tests ───────────────────────────────────────────────────
  if (!config.layers.testExecution) {
    results.summary = `${affected.length} affected test(s) identified but execution is disabled.`;
    return results;
  }

  // Check if playwright is available
  const playwrightAvailable = isPlaywrightAvailable();
  if (!playwrightAvailable) {
    console.log("  Playwright not available — reporting affected tests without execution.");
    results.summary = `${affected.length} affected test(s) identified. Install @playwright/test to enable execution.`;
    return results;
  }

  console.log(`\n  Executing ${affected.length} affected test(s)...`);
  results.executed = true;

  for (const test of affected) {
    const result = runSingleTest(test, config);
    if (result.passed) {
      results.passed.push(result);
      if (test.isQure) results.qureResults.passed.push(result);
    } else {
      results.failed.push(result);
      if (test.isQure) results.qureResults.failed.push(result);
    }
  }

  // ─── Coverage delta ───────────────────────────────────────────────────────────
  if (config.layers.coverageDelta) {
    results.coverageDelta = measureCoverageDelta(changedFiles, config);
  }

  // ─── Summary ──────────────────────────────────────────────────────────────────
  const passCount = results.passed.length;
  const failCount = results.failed.length;
  results.summary = `${passCount} passed, ${failCount} failed out of ${affected.length} affected test(s).`;

  if (failCount > 0) {
    console.log(`\n  FAILURES:`);
    results.failed.forEach((f) => {
      console.log(`    ✗ ${f.testName}: ${f.error.slice(0, 100)}`);
    });
  }

  return results;
}

// ─── Helpers ────────────────────────────────────────────────────────────────────

function collectTestFiles(config) {
  const tests = [];
  const dirs = [config.testDir, config.qureTestDir, ...(config.qure?.scanDirs || [])];
  const seen = new Set();

  for (const dir of dirs) {
    if (!dir || !fs.existsSync(dir)) continue;
    walkTests(dir, tests, seen);
  }

  return tests;
}

function walkTests(dir, results, seen) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      if (["node_modules", ".next", "dist", "build", ".git"].includes(entry.name)) continue;
      walkTests(full, results, seen);
    } else if (/\.(spec|test)\.(ts|tsx|js|jsx|mjs)$/.test(entry.name)) {
      if (seen.has(full)) continue;
      seen.add(full);

      const content = fs.readFileSync(full, "utf-8");
      const isQure = QURE_MARKERS.some((m) => m.test(content));

      results.push({
        filePath: full,
        content,
        isQure,
        testNames: extractTestNames(content),
        referencedFiles: extractReferencedFiles(content),
        referencedSelectors: extractSelectors(content),
        referencedUrls: extractUrls(content),
      });
    }
  }
}

function extractTestNames(content) {
  const names = [];
  const matches = content.matchAll(/(?:test|it)\(['"](.+?)['"]/g);
  for (const m of matches) names.push(m[1]);
  return names;
}

function extractReferencedFiles(content) {
  const refs = [];
  const matches = content.matchAll(/(?:import|require)\s*\(?\s*['"]([^'"]+)['"]/g);
  for (const m of matches) {
    if (!m[1].startsWith("@playwright")) refs.push(m[1]);
  }
  return refs;
}

function extractSelectors(content) {
  const selectors = [];
  const matches = content.matchAll(/(?:locator|getBy\w+)\(['"](.+?)['"]\)/g);
  for (const m of matches) selectors.push(m[1]);
  return selectors;
}

function extractUrls(content) {
  const urls = [];
  const matches = content.matchAll(/(?:goto|toHaveURL)\(['"]?([^'")\s]+)['"]?\)/g);
  for (const m of matches) urls.push(m[1]);
  return urls;
}

/**
 * Impact analysis: determine which tests are affected by the changed files.
 * A test is "affected" if it:
 * - Imports from a changed file
 * - References selectors that exist in changed files
 * - Tests URLs/routes that map to changed files
 */
function findAffectedTests(allTests, changedFiles) {
  const affected = [];
  const changedBases = changedFiles.map((f) => path.basename(f, path.extname(f)).toLowerCase());
  const changedDirs = changedFiles.map((f) => path.dirname(f).toLowerCase());

  for (const test of allTests) {
    let isAffected = false;
    let reason = "";

    // Check 1: Does the test import from a changed file?
    for (const ref of test.referencedFiles) {
      const refBase = path.basename(ref, path.extname(ref)).toLowerCase();
      if (changedBases.includes(refBase) || changedFiles.some((f) => ref.includes(f.replace(/\.(ts|tsx|js|jsx)$/, "")))) {
        isAffected = true;
        reason = `imports ${ref}`;
        break;
      }
    }

    // Check 2: Does the test reference URLs that map to changed route files?
    if (!isAffected) {
      for (const url of test.referencedUrls) {
        const urlParts = url.replace(/^\//, "").split("/").filter(Boolean);
        for (const part of urlParts) {
          if (changedBases.includes(part.toLowerCase()) || changedDirs.some((d) => d.includes(part.toLowerCase()))) {
            isAffected = true;
            reason = `tests route ${url}`;
            break;
          }
        }
        if (isAffected) break;
      }
    }

    // Check 3: Does the test reference selectors found in changed files?
    if (!isAffected) {
      for (const selector of test.referencedSelectors) {
        for (const file of changedFiles) {
          if (!fs.existsSync(file)) continue;
          try {
            const content = fs.readFileSync(file, "utf-8");
            // Check if the selector (as class name or id) appears in the changed file
            const selectorClean = selector.replace(/^[.#]/, "");
            if (content.includes(selectorClean)) {
              isAffected = true;
              reason = `selector "${selector}" found in ${path.basename(file)}`;
              break;
            }
          } catch (_) {}
        }
        if (isAffected) break;
      }
    }

    // Check 4: Qure tests that cover broad flows always count if ANY page they
    // visit intersects with a changed file's directory
    if (!isAffected && test.isQure) {
      for (const url of test.referencedUrls) {
        for (const changedFile of changedFiles) {
          if (changedFile.toLowerCase().includes(url.replace(/\//g, "").toLowerCase())) {
            isAffected = true;
            reason = `Qure flow visits ${url}, which intersects with ${path.basename(changedFile)}`;
            break;
          }
        }
        if (isAffected) break;
      }
    }

    if (isAffected) {
      affected.push({ ...test, affectedReason: reason });
    }
  }

  return affected;
}

function isPlaywrightAvailable() {
  try {
    execSync("npx playwright --version 2>&1", { encoding: "utf-8", timeout: 10_000, stdio: "pipe" });
    return true;
  } catch (_) {
    return false;
  }
}

function runSingleTest(test, config) {
  const testFile = test.filePath;
  const result = {
    filePath: testFile,
    testName: test.testNames[0] || path.basename(testFile),
    isQure: test.isQure,
    passed: false,
    error: "",
    duration: 0,
    stdout: "",
  };

  try {
    const start = Date.now();
    const output = execSync(
      `npx playwright test "${testFile}" --reporter=line 2>&1`,
      {
        encoding: "utf-8",
        timeout: config.layers.testTimeout || 60_000,
        stdio: "pipe",
        cwd: config.appDir || ".",
      }
    );
    result.passed = true;
    result.duration = Date.now() - start;
    result.stdout = output.slice(-500); // last 500 chars
  } catch (err) {
    result.passed = false;
    result.duration = Date.now() - start;
    result.error = (err.stdout || err.stderr || err.message || "").slice(0, 1000);
    result.stdout = result.error;
  }

  const icon = result.passed ? "✓" : "✗";
  const source = test.isQure ? "[Qure]" : "";
  console.log(`    ${icon} ${source} ${result.testName} (${result.duration}ms)`);

  return result;
}

